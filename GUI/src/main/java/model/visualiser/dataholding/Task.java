package model.visualiser.dataholding;

public class Task {
	
	private int appered;
	
	private int started;
	
	private int ended;
	
	private int rank;
	
	private int id;

	public int getAppered() {
		return appered;
	}

	public void setAppered(int appered) {
		this.appered = appered;
	}

	public int getStarted() {
		return started;
	}

	public void setStarted(int started) {
		this.started = started;
	}

	public int getEnded() {
		return ended;
	}

	public void setEnded(int ended) {
		this.ended = ended;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
}
